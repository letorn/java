public class DemoSort {
  public static void main(String[] args) {
    //演示字符串比较
    int a = "Tom".compareTo("Jerry");//比较
    System.out.println(a);//a应该>0
    
    a = "Ben".compareTo("Tom");
    System.out.println(a);//a应该<0
    
    a = "Tom".compareTo("Tom");//a应该==0
    System.out.println(a);
    
    
    
    
  }

}
